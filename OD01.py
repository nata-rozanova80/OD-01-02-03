# Шаг 1: Запросить строку у пользователя.
# Шаг 2: Привести строку к нижнему регистру для игнорирования регистра.
# Шаг 3: Удалить все пробелы и специальные символы из строки.
# Шаг 4: Проверить, равна ли строка своей обратной версии.
# Шаг 5: Если равны, вернуть True, иначе вернуть False.

def get_input():
    return input("Введите строку для проверки на палиндром: ")

def is_palindrome(s):
    s = s.lower()
    s = ''.join(char for char in s if char.isalnum())
    return s == s[::-1]

# Пример использования
user_input = get_input()
if is_palindrome(user_input):
    print("Строка является палиндромом.")
else:
    print("Строка не является палиндромом.")
